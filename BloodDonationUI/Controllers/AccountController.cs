using System;
using System.Globalization;
using System.Linq;
using System.Security.Claims;
using System.Threading.Tasks;
using System.Web;
using System.Web.Mvc;
using Microsoft.AspNet.Identity;
using Microsoft.AspNet.Identity.Owin;
using Microsoft.Owin.Security;
using BloodDonationUI.Models;

namespace BloodDonationUI.Controllers
{
    [Authorize]
    public class AccountController : Controller
    {
        ApplicationDbContext iddb = new ApplicationDbContext();
        BloodDonationEntities bddb = new BloodDonationEntities();

        private ApplicationSignInManager _signInManager;
        private ApplicationUserManager _userManager;

        public AccountController()
        {
        }

        public AccountController(ApplicationUserManager userManager, ApplicationSignInManager signInManager)
        {
            UserManager = userManager;
            SignInManager = signInManager;
        }

        public ApplicationSignInManager SignInManager
        {
            get
            {
                return _signInManager ?? HttpContext.GetOwinContext().Get<ApplicationSignInManager>();
            }
            private set
            {
                _signInManager = value;
            }
        }

        public ApplicationUserManager UserManager
        {
            get
            {
                return _userManager ?? HttpContext.GetOwinContext().GetUserManager<ApplicationUserManager>();
            }
            private set
            {
                _userManager = value;
            }
        }

        private void AttachLoginDataToSession(ApplicationUser user, BloodDonationEntities db)
        {
            SessionState.Current.UserEmail = user.Email;
            SessionState.Current.UserRole = user.Roles.Select(r => r.RoleId).FirstOrDefault();

            var lastName = db.Donor.Where(d => d.Email == user.Email).Select(u => u.LastName).FirstOrDefault();
            var firstName = db.Donor.Where(d => d.Email == user.Email).Select(u => u.FirstName).FirstOrDefault();

            SessionState.Current.UserName = (lastName != null || firstName != null) ? ((lastName + " " + firstName).Trim(' ')) : (null) ?? user.Email;
        }

        //
        // GET: /Account/Login
        [AllowAnonymous]
        public ActionResult Login()
        {
            return View();
        }

        //
        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            // This doesn't count login failures towards account lockout
            // To enable password failures to trigger account lockout, change to shouldLockout: true
            var result = await SignInManager.PasswordSignInAsync(model.Email, model.Password, model.RememberMe, shouldLockout: false);
            switch (result)
            {
                case SignInStatus.Success:

                    var user = iddb.Users.Where(u => u.Email == model.Email).FirstOrDefault();

                    AttachLoginDataToSession(user, bddb);

                    return RedirectToAction("Index", "Home");

                case SignInStatus.LockedOut:
                    return View("Lockout");

                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid login attempt.");
                    return View(model);
            }
        }

        //
        // GET: /Account/Register
        [AllowAnonymous]
        public ActionResult Register()
        {
            //retrieve data for dropdown lists on register page personal info section
            ViewBag.DonorSexList = bddb.DonorSex.ToList();
            ViewBag.DonorABOList = bddb.ABOBloodGroup.ToList();
            ViewBag.DonorRhList = bddb.RhBloodGroup.ToList();

            RegisterViewModel model = new RegisterViewModel();

            return View(model);
        }

        //
        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = new ApplicationUser { UserName = model.AccountInfo.Email, Email = model.AccountInfo.Email };
                var result = await UserManager.CreateAsync(user, model.AccountInfo.Password);
                if (result.Succeeded)
                {
                    await SignInManager.SignInAsync(user, isPersistent: false, rememberBrowser: false);

                    // For more information on how to enable account confirmation and password reset please visit https://go.microsoft.com/fwlink/?LinkID=320771
                    // Send an email with this link
                    // string code = await UserManager.GenerateEmailConfirmationTokenAsync(user.Id);
                    // var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
                    // await UserManager.SendEmailAsync(user.Id, "Confirm your account", "Please confirm your account by clicking <a href=\"" + callbackUrl + "\">here</a>");

                    //add user to Donor role
                    UserManager.AddToRole(user.Id, iddb.Roles.Where(r => r.Name == "Donor").Select(r => r.Name).FirstOrDefault());

                    //add personal data to BloodDonationDB Donor table
                    Donor newDonor = new Donor
                    {
                        Email = model.AccountInfo.Email,
                        BirthDate = model.PersonalInfo.BirthDate,
                        DonorSexId = model.PersonalInfo.DonorSexId,
                        SocialInsuranceNumber = model.PersonalInfo.SocialInsuranceNumber,
                        ABOBloodGroupId = model.PersonalInfo.ABOBloodGroupId,
                        RhBloodGroupId = model.PersonalInfo.RhBloodGroupId,
                        LastName = model.PersonalInfo.LastName,
                        FirstName = model.PersonalInfo.FirstName
                    };

                    using (var transaction = bddb.Database.BeginTransaction())
                    {
                        bddb.Donor.Add(newDonor);
                        await bddb.SaveChangesAsync();
                        transaction.Commit();
                    }

                    AttachLoginDataToSession(user, bddb);

                    return RedirectToAction("Index", "Home");
                }
                AddErrors(result);
            }

            // If we got this far, something failed, redisplay form
            ViewBag.DonorSexList = bddb.DonorSex.ToList();
            ViewBag.DonorABOList = bddb.ABOBloodGroup.ToList();
            ViewBag.DonorRhList = bddb.RhBloodGroup.ToList();

            return View(model);
        }

        //
        // GET: /Account/ConfirmEmail
        [AllowAnonymous]
        public async Task<ActionResult> ConfirmEmail(string userId, string code)
        {
            if (userId == null || code == null)
            {
                return View("Error");
            }
            var result = await UserManager.ConfirmEmailAsync(userId, code);
            return View(result.Succeeded ? "ConfirmEmail" : "Error");
        }

